def make_contact_sheet(paths, out_path: Path, title: str):
    thumb_w, thumb_h = 260, 170
    gap = 22
    cols = 4
    rows = (len(paths) + cols - 1) // cols
    width = cols * thumb_w + (cols + 1) * gap
    height = 90 + rows * thumb_h + (rows + 1) * gap
    image = Image.new("RGB", (width, height), "white")
    draw = ImageDraw.Draw(image)
    draw.rectangle([0, 0, width, 78], fill=(31, 78, 121))
    draw.text((width // 2, 38), title, fill="white", anchor="mm")
    for idx, path in enumerate(paths):
        src = Image.open(path).convert("RGB")
        src.thumbnail((thumb_w, thumb_h))
        canvas = Image.new("RGB", (thumb_w, thumb_h), (244, 247, 251))
        x = (thumb_w - src.width) // 2
        y = (thumb_h - src.height) // 2
        canvas.paste(src, (x, y))
        row, col = divmod(idx, cols)
        left = gap + col * (thumb_w + gap)
        top = 90 + gap + row * (thumb_h + gap)
        image.paste(canvas, (left, top))
        draw.rectangle([left, top, left + thumb_w, top + thumb_h], outline=(190, 200, 215), width=2)
    image.save(out_path)


