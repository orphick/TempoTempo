def hex_to_rgb(value):
    value = value.lstrip('#')
    return tuple(int(value[i:i + 2], 16) for i in (0, 2, 4))


def blend(start, end, ratio):
    return tuple(int(start[i] + (end[i] - start[i]) * ratio) for i in range(3))


def paragraphs(*items):
    return '\n\n'.join(item.strip() for item in items if item.strip())


def create_product_image(slug, colors, motif):
    out_dir = Path(settings.MEDIA_ROOT) / 'products' / 'demo'
    out_dir.mkdir(parents=True, exist_ok=True)
    image_path = out_dir / f'{slug}.png'

    width, height = 1200, 800
    start = hex_to_rgb(colors[0])
    end = hex_to_rgb(colors[1])
    image = Image.new('RGB', (width, height), start)
    draw = ImageDraw.Draw(image)
    for y in range(height):
        draw.line([(0, y), (width, y)], fill=blend(start, end, y / height))

    overlay = Image.new('RGBA', (width, height), (0, 0, 0, 0))
    art = ImageDraw.Draw(overlay)
    art.ellipse((-180, -180, 420, 420), fill=(255, 255, 255, 28))
